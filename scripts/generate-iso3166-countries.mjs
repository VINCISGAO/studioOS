import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = join(root, "lib/geo/iso3166-source.json");
const targetPath = join(root, "lib/geo/iso3166-countries.ts");

const source = JSON.parse(readFileSync(sourcePath, "utf8")) as Array<{
  name: string;
  "alpha-2": string;
  "alpha-3": string;
  region: string | null;
}>;

const rows = source.map((item) => {
  const region = item.region?.trim() || "Other";
  const name = item.name.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
  return `  ["${item["alpha-2"]}", "${item["alpha-3"]}", "${name}", "${region}"]`;
});

const output = `/** ISO 3166-1 — generated by scripts/generate-iso3166-countries.mjs. Do not edit by hand. */

export type Iso3166Row = readonly [iso2: string, iso3: string, nameEn: string, region: string];

export const ISO3166_ROWS: Iso3166Row[] = [
${rows.join(",\n")}
];

export const ISO3166_COUNTRY_COUNT = ISO3166_ROWS.length;
`;

writeFileSync(targetPath, output, "utf8");
console.log(`Wrote ${source.length} countries to ${targetPath}`);
